	function displayscreenshots($dir, $dirzip, $islocalized = false)
	{
		if($islocalized)
		{
			$array_languages = array("eng" => "English (PNG)", 
			"engjpg" => "English (JPEG)", 
			"fra" => "Français", 
			"ger" => "Deutsch",
			"spa" => "Español",
			"ita" => "Italiano",
			"rus" => "Russian",
			"brz" => "Português Brasileiro",
			"jap" => "Japanese",
			"chi" => "Simplified Chinese",
			"pol" => "Polski");
			
			echo '<div class="uk-alert" style="color:#FFFFFF">'. 'Download LOCALIZED screenshots as .zip below :<br />';
			$dirorig = $dirzip;
			
			$i = 0;
			foreach($array_languages as $key => $title)
			{
				$i++;
				$dirzip = $dirorig."_".$key;
				
				if( file_exists($dirzip.".zip") )
				{
					$filesize = filesize($dirzip.".zip");
					if( $filesize > 1024 && $filesize < 1048576 ) {
						$filesize = (int)( $filesize / 1024 ).'KB';
					}
					if( $filesize > 1048576 ) {
						$filesize = (int)(( $filesize / 1024 ) / 1024 ).'MB';
					}
					
					if($i > 1) echo " - ";
					echo '<a href="'. $dirzip.".zip".'">'. $title. '</a>';
				}
			}
			
			echo '</div>';
		}
		else if( file_exists($dirzip.".zip") )
		{
			$filesize = filesize($dirzip.".zip");
			if( $filesize > 1024 && $filesize < 1048576 ) {
				$filesize = (int)( $filesize / 1024 ).'KB';
			}
			if( $filesize > 1048576 ) {
				$filesize = (int)(( $filesize / 1024 ) / 1024 ).'MB';
			}

			echo '<a href="'. $dirzip.".zip".'"><div class="uk-alert">'. 'Download all pictures as .zip</div></a>';
		}
		

		echo '<div style = "float:left;">';
		$t_screenshots = array();
		$i = 0;
		if ($handle = opendir($dir))
		{
			$found = 0;
			while (false !== ($entry = readdir($handle)))
			{
				if( substr($entry,-4) == ".png" || substr($entry,-4) == ".jpg" || substr($entry,-4) == ".gif" )
				{
					if(!strpos($entry, "_tb"))
					{	
						$t_screenshots[$i++] = $entry;
						$found++;
					}
				}
			}
		}

		asort($t_screenshots);

		foreach($t_screenshots as $entry)
		{
			$filenameshort = substr($entry,0, strlen($entry) - 4);	
			$ext = substr($entry,-4);
			
			$entrytb = $entry;
			
			if(file_exists($dir.$filenameshort."_tb".$ext))
			{
				$entrytb = $filenameshort."_tb".$ext;
			}
			
			echo '<a href="'. $dir. $entry .'"><img src="'. $dir.$entrytb.'" alt="'.$entry.'" style="width: 30%; float:left; border:0; margin:2px;"/></a>';
		}


		echo '</div>
		<div style="float:left; width : 100%;"><br /></div>';

		closedir($handle);
	}
?>


